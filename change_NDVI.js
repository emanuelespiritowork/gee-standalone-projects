//          5.4 create a legend
function addLegend(title, palette, names) {
  var legend = ui.Panel({style: {position: 'bottom-left'}});
  legend.add(ui.Label({value: title, style: {fontWeight: 'bold', fontSize: '18px'}}));

  var makeRow = function(color, name) {
    var colorBox = ui.Label({
      style: {
        backgroundColor: color,
        padding: '8px',
        margin: '0'
      }
    });
    var description = ui.Label({
      value: name,
      style: {margin: '0 0 4px 6px'}
    });
    return ui.Panel({
      widgets: [colorBox, description],
      layout: ui.Panel.Layout.Flow('horizontal')
    });
  };

  for (var i = 0; i < palette.length; i++) {
    legend.add(makeRow(palette[i], names[i]));
  }
  Map.add(legend);
}

var palette_colors = ['black','yellow','red','blue','orange','green','lightblue'];
var names = ['forest to deforested', 'agriculture to deforested',
'forest to agriculture', 'agriculture to forest', 'deforested to agriculture',
'deforested to forest','low quality pixel'];

addLegend('Legend', palette_colors, names);


